void OLED_OFF(void)
{
	WriteCmd(0X8D);  //设置电荷泵
	WriteCmd(0X10);  //关闭电荷泵 
	WriteCmd(0XAE);  //OLED休眠
}

void Int_To_Str(int x,unsigned char *Str)
{
	int t;
	unsigned char *Ptr,Buf[5];
	int i = 0;
	Ptr = Str;
	if(x < 10)		                 // 当整数小于10，转换为0x格式
	{
		*Ptr ++ = '0'; 
		*Ptr ++ = x+0x30;
	}
	else
	{
		while(x > 0)
		{
			t = x % 10;
			x = x / 10;
			Buf[i++] = t+0x30;	// 通过计算把数字编成ASCII码形式
		}
		i -- ;
		for(;i >= 0;i --) 		// 将得到的字符串倒序
		{
			*(Ptr++) = Buf[i];
		}
	}
	*Ptr = '\0';
}


